 <section id="Story">
 <div class="Head-1">
 <h1 class="Header" data-aos="flip-right" style="color:#F84F39">OUR STORY</h1>
 </div>
 <p data-aos="fade-up">
  As a believer in the Lord Jesus Christ, I noticed a
  shift in the interest of teenagers and young adults
  from God and His word.</p>
   <p data-aos="zoom-in">They didn't find the Bible
  cool to read instead, they preferred Manga [Anime
  Lovers], Wattpad, social media and many other
  things that shifted their attention from the things of
  God. </p>
  <p data-aos="slide-down">Then I began to feel bad for them thinking all
  hope was lost, but later, it dawned on me that it is
  very possible to make the Bible and the things of
  God more interesting and fun.</P>
   <p data-aos="zoom-in-down">Yes! By bringing it to the level of the young adults,
   showing them how relatable the scriptures are to
   everyday life experiences and obviously, giving the
   interpretation, in Pidgin English makes it more
   interesting for a Nigerian youth.
   That's how "thebeliever's story" started.</p>
   <p data-aos="zoom-in-up">This name was chosen because anyone who has
  received Jesus into his/her life as his Lord
   and Savior is a believer in Jesus Christ, so what we have to
   do is to see how the believers in the Lord Jesus
   Christ lived here on Earth and how they overcame
   trials and temptations, how they acted or responded
   to situations and then bring it home to our time and
  live like they did.</p>
   <p data-aos="zoom-in-down">"Funny Interpretations" and "Bible Savages" will also
  put joy in your hearts and laugh on your faces.
  Are you ready? Let's go...</p>
